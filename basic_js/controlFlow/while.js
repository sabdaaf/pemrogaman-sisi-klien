/*cara menghentikan while dengan confirm*/
ulang = true;
while (ulang) {
  console.log("hello redemption welcome back");
  ulang = confirm("mau disapa lagi?");
}

/*cara menghentukam dengan termination/angka berhenti */
nilaiAwal = 1;
while (nilaiAwal <= 10) {
  console.log("angkot no." + nilaiAwal + " beroperasi dengan baik");
  nilaiAwal++;
}
